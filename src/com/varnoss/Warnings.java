package com.varnoss;

import java.util.List;

public class Warnings {
	List<Warning> warnings;

	public Warnings(List<Warning> warnings) {
		super();
		this.warnings = warnings;
	}

	public List<Warning> getWarnings() {
		return warnings;
	}

	public void setWarnings(List<Warning> warnings) {
		this.warnings = warnings;
	}
	
}
